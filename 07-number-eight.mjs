const add = (num) => {
  return 4 + num;
};

const subtract = (num) => {
  return 12 - num;
};

const multiply = (num) => {
  return 2 * num;
};

const divide = (num) => {
  return 32 / num;
};

console.log("Addition: ", add(4));
console.log("Subtraction: ", subtract(4));
console.log("Multiplication: ", multiply(4));
console.log("Division: ", divide(4));
